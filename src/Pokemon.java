public class Pokemon {
    private int number;
    private String Name;
    private String Level;
    private String XP;
    private int type;
    private int HP;
    private Move move1;

    public Pokemon(int number, String Name, String Level, String XP, int type, int HP, Move move1) {
        this.number = number;
        this.Name = Name;
        this.Level = Level;
        this.XP = XP;
        this.type = type;
        this.HP = HP;
        this.move1 = move1;
    }




}
